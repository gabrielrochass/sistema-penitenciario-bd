    DETENTO TP_DETENTO,

    CONSTRUCTOR FUNCTION TP_SENTENCA(SELF IN OUT NOCOPY TP_SENTENCA, CRIME VARCHAR2, DETENTO TP_DETENTO) RETURN SELF AS RESULT
